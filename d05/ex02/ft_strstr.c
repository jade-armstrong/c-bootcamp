char	*ft_strstr(char	*str, char *to_find)
{	
  char *a = str, *b = to_find;
  while (1) {
    if ( !*b ) return (char *) str;
    if ( !*a ) return NULL;
    if ( *a++ != *b++) { a = ++str; b = to_find; }
  }
}	
