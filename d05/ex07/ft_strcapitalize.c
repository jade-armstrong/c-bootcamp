char	*ft_strcapitalize(char *str)
{
	int i = 0;

	while (str[i] != '\0')
	{
		if ((i == 0 || 
    !((str[i - 1] >= '0' && str[i-1] <= '9') || 
    (str[i - 1] >= 'a' && str[i-1] <= 'z') || 
    (str[i - 1] >= 'A' && str[i-1] <= 'Z'))) 
    && (str[i] >= 'a' && str[i] <= 'z'))
		{
			str[i] = str[i] - 'a' + 'A';
		}
		else if (!(i == 0 || 
    !((str[i - 1] >= '0' && str[i-1] <= '9') || 
    (str[i - 1] >= 'a' && str[i-1] <= 'z') || 
    (str[i - 1] >= 'A' && str[i-1] <= 'Z'))) &&
    (str[i] >= 'A' && str[i] <= 'Z'))
		{
			str[i] = str[i] + 'a' - 'A';
		}
		i++;
	}
	return (str);
}
