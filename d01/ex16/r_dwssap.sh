#!/bin/sh
cat /etc/passwd | cut -d "#" -f 1 | sed '/^$/d' | sed -n '1d;p;n'| cut -d "_" -f 2 | cut -d ":" -f 1 | rev | sort -r | sed -n "$FT_LINE1,$FT_LINE2"p | awk '{print}' ORS=', ' | sed -e 's/, $/./'
